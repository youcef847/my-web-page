const questionElement = document.getElementById('question');
const answerButtons = document.getElementById('answers');
const nextButton = document.getElementById('next-btn');
const scoreContainer = document.getElementById('score-container');
const scoreText = document.getElementById('score-text');
const questionNumberText = document.getElementById('question-number');

let currentQuestionIndex = 0;
let score = 0;

function startQuiz() {
  currentQuestionIndex = 0;
  score = 0;
  scoreContainer.classList.add('hide');
  nextButton.innerText = "التالي";
  showQuestion();
}

function showQuestion() {
  resetState();
  let q = questions[currentQuestionIndex];
  questionNumberText.innerText = `السؤال ${currentQuestionIndex + 1} من ${questions.length}`;
  questionElement.innerText = q.question;

  q.answers.forEach((answer, index) => {
    const btn = document.createElement("button");
    btn.innerText = answer;
    btn.classList.add("answer-btn");
    btn.addEventListener("click", () => selectAnswer(btn, index));
    answerButtons.appendChild(btn);
  });
}

function resetState() {
  nextButton.style.display = "none";
  answerButtons.innerHTML = "";
}

function selectAnswer(button, index) {
  const correctIndex = questions[currentQuestionIndex].correct;
  const buttons = Array.from(answerButtons.children);
  buttons.forEach((btn, i) => {
    btn.disabled = true;
    if (i === correctIndex) {
      btn.classList.add("correct");
    } else if (i === index) {
      btn.classList.add("wrong");
    }
  });

  if (index === correctIndex) {
    score++;
  }

  nextButton.style.display = "inline-block";
}

function showScore() {
  resetState();
  questionElement.innerText = "";
  questionNumberText.innerText = "";
  scoreText.innerText = `أجبت بشكل صحيح على ${score} من ${questions.length} سؤالًا.`;
  scoreContainer.classList.remove("hide");
  nextButton.innerText = "ابدأ من جديد";
  nextButton.style.display = "inline-block";
}

